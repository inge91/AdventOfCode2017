def fix_pipes(pipes):
    for pipe in pipes:
        for connection in pipe[1:]:
            if pipe[0] not in pipes[connection][1:]:
                pipes[connection][1:] += [pipe[0]]

    return pipes


def reachable(reachable_list, current_index, complete_list):
    if current_index in reachable_list:
        return reachable_list
    reachable_list += [current_index]
    for connected in complete_list[current_index][1:]:
        reachable_list = reachable(reachable_list, connected, complete_list)
    return reachable_list


def get_unique(doubles):
    o = []
    for x in doubles:
        if x not in o:
            o += [x]
    return o

